// 소형 위젯 생성
async function createSmallWidget(quote) {
    const widget = new ListWidget();
    
    // 배경 설정
    const bgImage = await getBackgroundImage();
    const hasBg = bgImage !== null;
    
    if (hasBg) {
        widget.backgroundImage = bgImage;
        // 배경 이미지 위에 반투명 오버레이
        const gradient = new LinearGradient();
        gradient.locations = [0, 1];
        gradient.colors = [new Color("#000000", 0.3), new Color("#000000", 0.5)];
        widget.backgroundGradient = gradient;
    } else {
        const gradient = new LinearGradient();
        gradient.locations = [0, 1];
        gradient.colors = [COLORS.gradient1, COLORS.gradient2];
        widget.backgroundGradient = gradient;
    }
    
    widget.setPadding(12, 12, 12, 12);
    
    // 명언 텍스트
    const quoteText = widget.addText(quote.text);
    quoteText.font = Font.mediumSystemFont(13);
    quoteText.textColor = hasBg ? COLORS.textWithBg : COLORS.text;
    if (hasBg) quoteText.shadowColor = COLORS.shadowColor;
    if (hasBg) quoteText.shadowRadius = 2;
    quoteText.minimumScaleFactor = 0.7;
    quoteText.lineLimit = 4;
    
    widget.addSpacer();
    
    // 저자/출처
    const authorText = widget.addText(`- ${quote.book}`);
    authorText.font = Font.italicSystemFont(10);
    authorText.textColor = hasBg ? COLORS.authorWithBg : COLORS.author;
    if (hasBg) authorText.shadowColor = COLORS.shadowColor;
    if (hasBg) authorText.shadowRadius = 1;
    authorText.lineLimit = 1;
    
    return widget;
}

