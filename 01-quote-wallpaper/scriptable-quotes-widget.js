// ==========================================

async function createWidget() {
    // 명언 선택
    const availableQuotes = getFilteredQuotes();
    const selectedQuote = selectRandomQuote(availableQuotes);
    
    // 위젯 크기에 따라 다른 위젯 생성
    let widget;
    const widgetFamily = config.widgetFamily;
    
    switch (widgetFamily) {
        case 'small':
            widget = await createSmallWidget(selectedQuote);
            break;
        case 'medium':
            widget = await createMediumWidget(selectedQuote);
            break;
        case 'large':
            widget = await createLargeWidget(selectedQuote);
            break;
        default:
            // 위젯이 아닌 경우 중형으로 기본 설정
            widget = await createMediumWidget(selectedQuote);
    }
    
    // 위젯 탭 시 스크립트 실행 (새 명언 표시)
    widget.url = "scriptable:///run/" + encodeURIComponent(Script.name());
    
    // 새로고침 시간 설정 (15분 - iOS 최소 권장 시간)
    const nextRefresh = new Date();
    nextRefresh.setMinutes(nextRefresh.getMinutes() + 15);
    widget.refreshAfterDate = nextRefresh;
    
    return widget;
}

// 위젯 실행 또는 미리보기
const widget = await createWidget();

if (config.runsInWidget) {
    // 위젯으로 실행
    Script.setWidget(widget);
} else {
    // Scriptable 앱 내에서 미리보기
    switch (config.widgetFamily) {
        case 'small':
            widget.presentSmall();
            break;
        case 'large':
            widget.presentLarge();
            break;
        default:
            widget.presentMedium();
    }
}

Script.complete();
